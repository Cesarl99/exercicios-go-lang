package main

import (
	"fmt"
)

func main() {
	x := 100
	if x < 100 {
		fmt.Println("o valor de x e menor que 100")
	} else if x == 100 {
		fmt.Println("o valor de x e igual 100")
	} else {
		fmt.Println("o valor de x e maior que 100")
	}
}


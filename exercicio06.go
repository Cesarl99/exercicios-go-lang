package main

import (
	"fmt"
)

func main() {
	valor := 1236
	fmt.Printf("o numero %v em decimal é : %d\n", valor, valor)
	fmt.Printf("o numero %v em binário é : %b\n", valor, valor)
	fmt.Printf("o  numero %v em hexadecimal é : %#x\n", valor, valor)

}

